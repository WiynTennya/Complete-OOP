// Abstract
abstract class Student {
    protected String name;
    protected String id;

    public Student(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public abstract void displayInfo();
}

// Inheritance + Polymorphism
class UndergraduateStudent extends Student {
    public UndergraduateStudent(String name, String id) {
        super(name, id);
    }

    @Override
    public void displayInfo() {
        System.out.println("Undergraduate Student: " + name + " - ID: " + id);
    }
}

class GraduateStudent extends Student {
    public GraduateStudent(String name, String id) {
        super(name, id);
    }

    @Override
    public void displayInfo() {
        System.out.println("Graduate Student: " + name + " - ID: " + id);
    }
}

// Encapsulation
class StudentData {
    private String name;
    private String id;

    public StudentData(String name, String id) {
        this.name = name;
        this.id = id;
    }

    // Getters
    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    // Setters
    public void setName(String newName) {
        this.name = newName;
    }

    public void setId(String newId) {
        this.id = newId;
    }
}

// Main program
public class Main {
    public static void main(String[] args) {

        System.out.println("=== Encapsulation ===");
        StudentData data = new StudentData("Raden", "12345678");
        System.out.println("Name: " + data.getName());
        System.out.println("ID: " + data.getId());


        data.setName("Raden Hakim");
        data.setId("87654321");
        System.out.println("Updated Name: " + data.getName());
        System.out.println("Updated ID: " + data.getId());

        System.out.println("\n=== Inheritance and Polymorphism ===");
        Student s1 = new UndergraduateStudent("Budi", "U112233");
        Student s2 = new GraduateStudent("Yanto", "G223344");

        // Polymorphism: 
        s1.displayInfo();
        s2.displayInfo();
    }
}
